import math  # Імпортуємо модуль math для використання математичних функцій, зокрема числа π.

class Cylinder:
    def __init__(self, radius, height):
        """
        Конструктор класу Cylinder.
        
        :param radius: Радіус основи циліндра.
        :param height: Висота циліндра.
        """
        self.radius = radius  # Зберігаємо радіус як атрибут об'єкта.
        self.height = height  # Зберігаємо висоту як атрибут об'єкта.

    def volume(self):
        """
        Обчислює об'єм циліндра.
        
        :return: Об'єм циліндра, обчислений за формулою V = πr²h.
        """
        return math.pi * (self.radius ** 2) * self.height  # Використовуємо формулу для обчислення об'єму.

# Код нижче виконується, коли файл запускається безпосередньо.
if __name__ == "__main__":
    # Запитуємо у користувача введення значення радіусу.
    radius = float(input("Введіть радіус основи циліндра: "))
    
    # Запитуємо у користувача введення значення висоти.
    height = float(input("Введіть висоту циліндра: "))
    
    # Створюємо об'єкт класу Cylinder, передаючи введені радіус і висоту.
    cylinder = Cylinder(radius, height)
    
    # Обчислюємо об'єм циліндра за допомогою методу volume() і виводимо результат.
    print(f"Об'єм циліндра: {cylinder.volume():.2f}")